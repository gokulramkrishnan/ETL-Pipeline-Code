def generate_staging_model(output_dir: str, source_table: str, columns: list,
                            bk_columns: list, load_date_col: str, hash_algorithm: str,
                            entity: str, hash_key_col: str) -> str:
    """Generate a staging model SQL using datavault4dbt staging macro."""
    model_name = f"stg_{source_table.lower()}"
    col_list   = ",\n    ".join([f"{c['name']}" for c in columns])
    hash_fn    = "MD5" if hash_algorithm == "MD5" else "SHA2"
    bk_concat  = "||'||'||".join([f"CAST({bk} AS VARCHAR)" for bk in bk_columns])

    content = f"""-- Staging model: {model_name}
-- Source: {source_table}
-- Generated by gokulram.krishnan (datavault4dbt aligned)
{{{{ config(materialized='view', tags=['staging']) }}}}

WITH source AS (
    SELECT
        {col_list},
        CURRENT_TIMESTAMP()                        AS {load_date_col},
        '{{{{ var("record_source") }}}}'           AS RECORD_SOURCE
    FROM {{{{ source('{source_table.lower()}_src', '{source_table}') }}}}
),

hashed AS (
    SELECT
        {hash_fn}({bk_concat})                     AS {hash_key_col},
        *
    FROM source
)

SELECT * FROM hashed
"""
    path = f"{output_dir}/models/staging/{model_name}.sql"
    _write(path, content)
    return model_name


def generate_hub_model(output_dir: str, hub: dict) -> str:
    """Generate Hub model using datavault4dbt hub macro."""
    model_name  = hub["hub_name"].lower()
    dv4         = hub["dv4dbt"]
    src_bk_yaml = "\n      - ".join(dv4["src_bk"])

    content = f"""-- Hub model: {hub['hub_name']}
-- Entity: {hub['entity']}
-- Generated by gokulram.krishnan (datavault4dbt aligned)
{{{{ config(
    materialized='incremental',
    unique_key='{dv4["src_pk"]}',
    tags=['raw_vault','hub']
) }}}}

{{{{- datavault4dbt.hub(
    src_pk='{dv4["src_pk"]}',
    src_bk=[
      '{src_bk_yaml}'
    ],
    src_ldts='{dv4["src_ldts"]}',
    src_source='{dv4["src_source"]}',
    source_model='{dv4["source_model"]}'
) -}}}}
"""
    path = f"{output_dir}/models/raw_vault/hubs/{model_name}.sql"
    os.makedirs(os.path.dirname(path), exist_ok=True)
    _write(path, content)
    return model_name


def generate_link_model(output_dir: str, link: dict) -> str:
    """Generate Link model using datavault4dbt link macro."""
    model_name = link["link_name"].lower()
    dv4        = link["dv4dbt"]
    fk_yaml    = "\n      - ".join(dv4["src_fk"])
    src_models = dv4["source_model"] if isinstance(dv4["source_model"], list) else [dv4["source_model"]]
    src_model_yaml = "\n      - ".join(src_models)

    content = f"""-- Link model: {link['link_name']}
-- Entities: {link['entity_a']} <-> {link['entity_b']}
-- Generated by gokulram.krishnan (datavault4dbt aligned)
{{{{ config(
    materialized='incremental',
    unique_key='{dv4["src_pk"]}',
    tags=['raw_vault','link']
) }}}}

{{{{- datavault4dbt.link(
    src_pk='{dv4["src_pk"]}',
    src_fk=[
      '{fk_yaml}'
    ],
    src_ldts='{dv4["src_ldts"]}',
    src_source='{dv4["src_source"]}',
    source_model=[
      '{src_model_yaml}'
    ]
) -}}}}
"""
    path = f"{output_dir}/models/raw_vault/links/{model_name}.sql"
    os.makedirs(os.path.dirname(path), exist_ok=True)
    _write(path, content)
    return model_name


def generate_satellite_model(output_dir: str, sat: dict) -> str:
    """Generate Satellite model using datavault4dbt sat macro."""
    model_name  = sat["sat_name"].lower()
    dv4         = sat["dv4dbt"]
    payload_yaml = "\n      - ".join(dv4["src_payload"])
    hd_cols_yaml = "\n        - ".join(dv4["src_hashdiff"]["columns"])

    content = f"""-- Satellite model: {sat['sat_name']}
-- Entity: {sat['entity']} | Source: {sat['source_table']}
-- Generated by gokulram.krishnan (datavault4dbt aligned)
{{{{ config(
    materialized='incremental',
    unique_key=['{dv4["src_pk"]}', '{dv4["src_ldts"]}'],
    tags=['raw_vault','satellite']
) }}}}

{{{{- datavault4dbt.sat(
    src_pk='{dv4["src_pk"]}',
    src_hashdiff={{
        'is_hashdiff': true,
        'columns': [
          '{hd_cols_yaml}'
        ]
    }},
    src_payload=[
      '{payload_yaml}'
    ],
    src_ldts='{dv4["src_ldts"]}',
    src_source='{dv4["src_source"]}',
    source_model='{dv4["source_model"]}'
) -}}}}
"""
    path = f"{output_dir}/models/raw_vault/satellites/{model_name}.sql"
    os.makedirs(os.path.dirname(path), exist_ok=True)
    _write(path, content)
    return model_name


def generate_sources_yml(output_dir: str, metadata: dict):
    """Generate sources.yml for all source tables."""
    sources_block = {"version": 2, "sources": []}
    for src in metadata.get("sources", []):
        table = src.get("table_name","")
        sources_block["sources"].append({
            "name":     f"{table.lower()}_src",
            "database": "SOURCE_DB",    # replace with actual source DB
            "schema":   src.get("schema","OPERATIONAL"),
            "tables":   [{"name": table}],
        })
    path = f"{output_dir}/models/staging/sources.yml"
    _write(path, yaml.dump(sources_block, default_flow_style=False, sort_keys=False))


def generate_schema_yml(output_dir: str, dv_model: dict):
    """Generate schema.yml with column-level tests for hubs, links, satellites."""
    schema = {"version": 2, "models": []}

    for hub in dv_model.get("hubs", []):
        schema["models"].append({
            "name":        hub["hub_name"].lower(),
            "description": f"Hub for {hub['entity']} entity",
            "columns": [
                {"name": hub["hash_key"],
                 "description": "Hash key (primary key)",
                 "tests": ["unique", "not_null"]},
                *[{"name": bk, "tests": ["not_null"]} for bk in hub["business_keys"]],
                {"name": hub["ldts"],   "tests": ["not_null"]},
                {"name": hub["rsrc"],   "tests": ["not_null"]},
            ]
        })

    for link in dv_model.get("links", []):
        schema["models"].append({
            "name":        link["link_name"].lower(),
            "description": f"Link between {link['entity_a']} and {link['entity_b']}",
            "columns": [
                {"name": link["hash_key"],   "tests": ["unique","not_null"]},
                {"name": link["hub_a_hk"],   "tests": ["not_null"]},
                {"name": link["hub_b_hk"],   "tests": ["not_null"]},
                {"name": link["ldts"],       "tests": ["not_null"]},
            ]
        })

    for sat in dv_model.get("satellites", []):
        schema["models"].append({
            "name":        sat["sat_name"].lower(),
            "description": f"Satellite for {sat['entity']} from {sat['source_table']}",
            "columns": [
                {"name": sat["hub_hash_key"], "tests": ["not_null"]},
                {"name": sat["hashdiff"],     "tests": ["not_null"]},
                {"name": sat["ldts"],         "tests": ["not_null"]},
            ]
        })

    path = f"{output_dir}/models/raw_vault/schema.yml"
    _write(path, yaml.dump(schema, default_flow_style=False, sort_keys=False))



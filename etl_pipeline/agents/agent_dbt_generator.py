class DBTGeneratorAgent:
    def run(self, metadata: dict, sttm: dict, dv_model: dict,
            output_dir: str, project_name: str, profile_name: str,
            target_database: str, schema_raw: str, schema_biz: str,
            schema_stg: str, datavault4dbt_version: str,
            hash_algorithm: str, record_source: str, load_date_col: str) -> dict:

        print("  Generating dbt project structure...")

        # ── Project config files ──────────────────────────────────────────────
        generate_packages_yml(output_dir, datavault4dbt_version)
        generate_dbt_project_yml(output_dir, project_name, profile_name,
                                  schema_raw, schema_biz, schema_stg,
                                  hash_algorithm, load_date_col, record_source)
        generate_profiles_yml(output_dir, profile_name, target_database)
        generate_sources_yml(output_dir, metadata)
        print("    ✅ dbt_project.yml, packages.yml, profiles.yml, sources.yml")

        # ── Staging models ────────────────────────────────────────────────────
        from agents.agent_data_vault import infer_business_keys, infer_entity_name, hash_key_col
        bk_map = infer_business_keys(metadata.get("sources",[]))
        staging_models = []
        for src in metadata.get("sources",[]):
            table   = src.get("table_name","")
            entity  = infer_entity_name(table)
            hk_col  = hash_key_col(entity)
            bk_cols = bk_map.get(table,[])
            m = generate_staging_model(
                output_dir, table, src.get("columns",[]),
                bk_cols, load_date_col, hash_algorithm, entity, hk_col
            )
            staging_models.append(m)
        print(f"    ✅ {len(staging_models)} staging model(s)")

        # ── Raw Vault models ──────────────────────────────────────────────────
        hub_models  = [generate_hub_model(output_dir, h) for h in dv_model.get("hubs",[])]
        link_models = [generate_link_model(output_dir, l) for l in dv_model.get("links",[])]
        sat_models  = [generate_satellite_model(output_dir, s) for s in dv_model.get("satellites",[])]
        print(f"    ✅ {len(hub_models)} hub(s), {len(link_models)} link(s), {len(sat_models)} satellite(s)")

        # ── Schema + README ───────────────────────────────────────────────────
        generate_schema_yml(output_dir, dv_model)
        generate_readme(output_dir, project_name, dv_model, datavault4dbt_version)
        print("    ✅ schema.yml + README.md")

        return {
            "staging_models":   len(staging_models),
            "raw_vault_models": len(hub_models) + len(link_models) + len(sat_models),
            "schema_files":     1,
        }
